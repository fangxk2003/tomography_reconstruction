import matplotlib.pyplot as plt
import numpy as np
N = 30
mse_values = []
with open('MSE_40d.txt', 'r') as file:
    for line in file:
        if line.startswith("MSE: "):
            mse_values.append(float(line.split(":")[1].strip()))
plt.plot(range(1, N + 1), np.log(mse_values), marker='o')
plt.xlabel('Iteration')
plt.ylabel('Log(MSE)')
plt.title('Log of MSE over Iterations')
plt.grid(True)
plt.show()